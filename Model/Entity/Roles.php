<?php

namespace App\Model\Entity;

class Roles {
    private string $role;

    /**
     * @return string
     */
    public function getRole(): string
    {
        return $this->role;
    }

    /**
     * @param string $role
     */
    public function setRole(string $role): self
    {
        $this->role = $role;
        return $this;
    }


}